#!/bin/bash

if [[ "${UID}" -eq 0 ]]
then
	echo "--------------------------------------------------------------------------------------------------------------"
	echo "Before "
	free -h
	echo "--------------------------------------------------------------------------------------------------------------"
	swapoff -a && swapon -a
	su -c "echo 3 >'/proc/sys/vm/drop_caches' && swapoff -a && swapon -a && printf '\n%s\n' 'Ram-cache and Swap Cleared'" root
	echo "--------------------------------------------------------------------------------------------------------------"
	echo "After"
	free -h
	echo "--------------------------------------------------------------------------------------------------------------"
else
  echo 'Run as root or sudo user.'
fi

